export function BottomNav() {
  const location = useLocation();
  const navigate = useNavigate();
  const { isWorkoutActive } = useWorkoutContext();

  // Hide bottom nav during active workout
  if (isWorkoutActive && location.pathname === '/workout') return null;

  return (
    <nav className="fixed bottom-0 left-0 right-0 z-40 bg-[#0d0d15]/95 backdrop-blur-lg border-t border-slate-800">
      <div className="max-w-lg mx-auto flex items-center justify-around h-16 pb-safe">
        {tabs.map((tab) => {
          const isActive = location.pathname === tab.path;
          return (
            <button
              key={tab.path}
              onClick={() => navigate(tab.path)}
              className={`flex flex-col items-center justify-center gap-0.5 w-16 h-full transition-colors ${
                isActive ? 'text-indigo-400' : 'text-slate-500 hover:text-slate-400'
              }`}
            >
              {tab.icon}
              <span className="text-[10px] font-medium">{tab.label}</span>
            </button>
          );
        })}
      </div>
    </nav>
  );
}
